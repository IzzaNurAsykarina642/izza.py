#variabel
a=10
b=5

# Penjumlahan 
hasil_penjumlahan = a + b
print(hasil_penjumlahan) # output = 15.0

# pengurangan 
hasil_pengurangan = a - b
print(hasil_pengurangan) # output = 5.0

# perkalian 
hasil_perkalian = a * b
print(hasil_perkalian) # output = 50.0

# pembagian 
hasil_pembagian = a / b
print(hasil_pembagian)

#variabel baru
a = 10
b = 3

# pembagian bulat
hasil_pembagianb = a // b
print(hasil_pembagianb) # output = 3

# modulus 
hasil_modulus = a % b
print(hasil_modulus) # output = 1

# mengubah isi variabel
a = 2
b = 3

# pangkat
hasil_pangkat = a ** b
print(hasil_pangkat) # output = 8

# operator gabungan
a = 10 
a += 5 # a menjadi 15
a -= 3 # a menjadi 12
a *= 2 # a menjadi 24
